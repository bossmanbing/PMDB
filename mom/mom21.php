		<h2>Mind Over Meta 21 - Embracing Your Weaknesses</h2>
<h5>Presented by SmashCapps</h5>
<p>Hello everyone, Orangegluon here. We are trying to get back on regular schedule, and this week we have a very, very special and inspiring guest article from SmashCapps on part of his own personal journey in Smash. We're really grateful for him taking the time to write this up, and it's worth reading, for every player in every competitive and non-competitive video game, and in life. We'll hand it over to him.
</p>
<hr />
<p>I wanted to open up and share a very personal story from my life that I thought might help those who read it grow as competitors. I'm hoping to help some folks avoid the mistakes I have made, and learn the lesson I did in a much easier way. I'm here to talk today about embracing your weaknesses.</p>

<h4>It Begins</h4>

<p><em>“Anyone who has lost something they thought was theirs forever finally comes to realize that nothing really belongs to them.” - Paulo Coelho</em></p>

<p>At age nine, after a day of shopping and enjoying an awesome swim at the pool, I got in bed to get some rest excited to have another active and exciting day tomorrow. The next morning I woke up in excruciating pain, unable to walk, and barely able to move. A rushed hospital trip and many tests later I was told I had <a target=_'blank' href='http://en.wikipedia.org/wiki/Guillain%E2%80%93Barr%C3%A9_syndrome'>Guillain-Barré Syndrome</a>, a <a href='http://www.ninds.nih.gov/disorders/gbs/detail_gbs.htm' target='_blank'>rare disease</a> that would permanently damage my entire nervous system for life. I didn't know if I could walk on my own even again, and I was stuck fighting through intense amounts of pain. There were talks of possibly even putting me on a ventilator. I had no idea if I could ever live life or do the things I liked to do ever again.
I refused to let any of this happen. I not only managed to get back up on my feet, but did so so quickly the doctors were baffled at the speed of my recovery. I now only need braces on my legs to help me on longer walks instead of a wheelchair or walker. I never needed the ventilator, and managed to even go back to playing some of the sports I once loved.
I can still play video games, a passion I've always had but I run into a lot of problems with severe hand pain, and since my nerves are damaged my reaction times can be much poorer than the average player.</p>
<hr />
<h4>The Now</h4>

<p><em>"There is no such thing as accident; it is fate misnamed." - Napoleon Bonaparte</em></p>

<p>So I made a "terrible mistake", I fell in love with Super Smash Bros. A series of games that basically is everything my hands cannot do became my obsession. I began training constantly, trying to increase my skills. My hands would burn, stinging and pulsing in pain for hours afterwards but I would put hours and hours into practice determined that like all of the other challenges in front of me all of this could be overcome with hard enough work.</p>

<p>All it did was force me to continuously bash against an impenetrable wall that I could never break through, as it simply wasn't possible. I fell into a serious depression. I would go and pick up the controller determined to try just ONE more time only to end up sobbing for hours afterwards. I couldn't stand having had so many things taken away from me by this disability to lose one more, it was a line drawn in the sand I could not cross. I constantly would have thoughts like "with this disability you will NEVER stand a chance, give up". This continued for quite some time, and I'm not proud that I let myself go through such a destructive cycle.</p>

<p>The worst part however, is all of this could have EASILY been avoided however, if I had just done one thing: embrace my weakness.</p>

<hr />
<h4><a href='https://www.youtube.com/watch?v=ZPc1H5u_ZyA' target='_blank'>Brolylegs</a></h4>

<p><em>"The thing about living with any disability is that you adapt; you do what works for you." - Stella Young</em></p>

<p>I owe quite a bit to one player of many fighting games in finally helping me realize what I was doing wrong, Brolylegs. I had heard of him and seen matches he had played in before and was always impressed on how well he could do with his physical limitations. Oddly enough this made me rag on myself even worse, "he can do it, why can't you?". This was until I saw him in an interview and had my moment of realization.</p>

<p><a href='https://www.youtube.com/watch?v=83nSodg-HTU' target='_blank'>"Look for what you CAN do."</a> he said, and spoke on how he had picked his main and control scheme based on his limitations and used them to the best of his advantage.</p>

<p>Here is a man who had to sometimes play in tournaments on a table, used his mouth to work the controls, and instead of looking down on himself took what he had and pressed forward with it. I don't think the word "humbling" was strong enough to describe how I felt.</p>
<hr />
<h4>How I Fixed This</h4>

<p><em>“The measure of intelligence is the ability to change.” - Albert Einstein</em></p>

<p>I had been an idiot. Of course I couldn't perform an insane amount of tech skill, and I had been dumb to even try. I sat back down to really look at Melee and figure out who would be a good fit for me. With my hands, l-canceling a lot or often really wasn't something I could or should be doing. So I picked the only character in the game who COULDN'T l-cancel some of his attacks, Mr. Game and Watch. With the pressure gone to try and keep up with these techs, I was able to start focusing on my fundementals more, learn my spacing, and actually get to playing the game. The opportunity was no longer there to beat myself up over missing an l-cancel or not getting a lot of tough tech down a lot of top tiers used, and I started to prosper.</p>

<a href='http://www.reddit.com/r/SSBPM/comments/2otfuk/mind_over_meta_4_autopilot_malfunction/' target='_blank'>Mind Over Meta 4</a> mentions <a href='https://docs.google.com/document/d/1iLlO7zpAW8arixpWRMJvG9XoXQmt9NMDTjZVWxsBDNA/edit' target='_blank'>an article that PPMD wrote</a> regarding motivation:</p>

<p>There are two reasons to play this game that allow for maximal improvement, enjoyment of the game, and keep the conscious cleared from focusing on judgments. These two motivations are: </p>

  <p>Playing to:
    <ul>
    <li>have fun</li>
    <li>learn</li>
  </ul>
  </p>

<p>I have now done this for every game I play in the series. In Smash 64 I now play as Donkey Kong. It's hard to not enjoy the DK hype, but his <a href='https://www.youtube.com/watch?v=MgPDFsiQODM&amp;t=0m13s' target='_blank'>ridiculous infinite chaingrab</a> helps me bridge the distance a bit in matches. For Brawl I chose King Dedede who yet again had a chaingrab, but also was heavy so he could help me when I would sometimes not react fast enough for the best DI, and his hard hitting attacks could help me come back when I inevitably made mistakes with my fingers. For Project M I took on Bowser, knowing with clever use of armor I might be able to eat through my opponents. In Smash 4 while I use Mii Swordfighter for fun, but by far my best character is Little Mac. With little button presses needed, and not tons of jumping, fast falling, and things like that it plays right into my weakness so I can press what advantages I DO have.</p>

<p>On top of this, instead of spending hours beating my hands into a pulp by practicing for hours on end I started to hit the books instead. Reading, learning every tiny trick there is, studying how each move interacts with others to try and use pure knowledge to overcome what my hands cannot.</p>

<p>I can easily say since I have started doing all of these things the depression has faded and I have finally started to improve as a player.</p>
<hr />
<h4>How To Embrace Your Own Weakness</h4>

<p><em>"I discovered that my insecurities and my flaws were things that I actually need to embrace, and I let them become my superpowers. - Skylar Grey</em></p>

<p>Not everyone has a physical limitation when it comes to playing, but if anyone out there faces a situation where physically playing is a problem, I encourage you try out out lots of strange controllers, try weird control setups, and do what you can. Trust me <a href='https://www.youtube.com/watch?v=_P-GJkFi_Ck' target='_blank'>there is a controller for everyone</a>.</p>

<p>But for those who's weaknesses may be more in the mental realm of things, take a page from my book and try to take a weakness you have and use it to improve your game.</p>

<p>Maybe you shield far too often within games, EMBRACE IT! While it is good to get rid of bad habits, maybe pick up a character with stronger OOS options to help you on the way until you get there. Another option could be to set your shield button somewhere completely new to force yourself to mentally recognize every time you go to shield.</p>

<p>Maybe you have a hard time when people get right in your face. While trying to learn the best way to deal with this, find a character with strong disjointed moves to keep the opponent away from your face and at range.</p>

<p>Have an issue dealing with projectiles that force you to approach? Grab your own to fling right back! Give yourself a tool to deal with the situation while learning how this is done and how to avoid it yourself.</p>
<hr />

<p>These are but a few examples, and I am certain there are many more. While it seems controversial compared to the usual advice of eliminating all weakness, I encourage readers who have hit these walls to instead take this new approach. You will find it will make your mental state in Smash MUCH better, and you will not only become a stronger player, but a stronger person as well.</p>

